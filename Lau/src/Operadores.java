public class Operadores {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println("El valor de a es: " + a);
        System.out.println("El valor de a es: " + b);
        System.out.println("El valor de a&&b es: " + (a&&b));
        System.out.println("El valor de a&&b es: " + (a||b));
        System.out.println("El valor de !(a||b) es: " + !(a||b));
    }

    }
